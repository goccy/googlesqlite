// render_results turns the raw `go test -bench` output captured at
// runtime into bench/RESULTS.md, a per-query performance report for
// the googlesqlite driver.
//
// Usage:
//
//	cd bench
//	go test -bench=. -benchmem -benchtime=200ms -run=^$ \
//	    -timeout 1800s . > bench.out
//	go run render_results.go bench.out > RESULTS.md
//
// The report lists ns/op, B/op and allocs/op for each corpus query.
// To detect a regression, compare a fresh run against a saved
// baseline RESULTS.md.
//
// The build tag keeps this file out of the test binary.
//go:build render_results

package main

import (
	"bufio"
	"fmt"
	"os"
	"sort"
	"strconv"
	"strings"
)

type entry struct {
	category string // window | aggregate | scalar | mixed | tpch_like
	name     string
	nsPerOp  float64
	bytesOp  int64
	allocsOp int64
}

func main() {
	if len(os.Args) < 2 {
		fmt.Fprintln(os.Stderr, "usage: render_results <bench.out>")
		os.Exit(2)
	}
	f, err := os.Open(os.Args[1])
	must(err)
	defer f.Close()

	scanner := bufio.NewScanner(f)
	scanner.Buffer(make([]byte, 0, 1<<20), 1<<20)

	var entries []entry
	var compileNS int64 = -1
	for scanner.Scan() {
		line := scanner.Text()
		if ns, ok := parseCompileLine(line); ok {
			compileNS = ns
			continue
		}
		e, ok := parseLine(line)
		if !ok {
			continue
		}
		entries = append(entries, e)
	}
	must(scanner.Err())

	matrix := groupByCategoryName(entries)
	render(os.Stdout, matrix, compileNS)
}

// parseCompileLine recognises the `wasm-compile-ns <n>` line emitted by
// the benchmark's TestMain — the one-time wazero AOT compilation cost.
func parseCompileLine(line string) (int64, bool) {
	const prefix = "wasm-compile-ns "
	if !strings.HasPrefix(line, prefix) {
		return 0, false
	}
	ns, err := strconv.ParseInt(strings.TrimSpace(line[len(prefix):]), 10, 64)
	if err != nil {
		return 0, false
	}
	return ns, true
}

func parseLine(line string) (entry, bool) {
	// Example:
	// BenchmarkCorpus/window/lag_lead-10  10  4123456 ns/op  1234 B/op  56 allocs/op
	if !strings.HasPrefix(line, "BenchmarkCorpus") {
		return entry{}, false
	}
	fields := strings.Fields(line)
	if len(fields) < 7 {
		return entry{}, false
	}
	name := fields[0]
	category, casename, ok := splitName(name)
	if !ok {
		return entry{}, false
	}
	ns, err := strconv.ParseFloat(fields[2], 64)
	if err != nil {
		return entry{}, false
	}
	bytesOp, err := strconv.ParseInt(fields[4], 10, 64)
	if err != nil {
		return entry{}, false
	}
	allocsOp, err := strconv.ParseInt(fields[6], 10, 64)
	if err != nil {
		return entry{}, false
	}
	return entry{
		category: category, name: casename,
		nsPerOp: ns, bytesOp: bytesOp, allocsOp: allocsOp,
	}, true
}

func splitName(full string) (category, name string, ok bool) {
	// BenchmarkCorpus/window/lag_lead-10
	body := full
	// Strip any "-NN" cpu-count suffix.
	if idx := strings.LastIndex(body, "-"); idx >= 0 {
		if _, err := strconv.Atoi(body[idx+1:]); err == nil {
			body = body[:idx]
		}
	}
	parts := strings.Split(body, "/")
	if len(parts) != 3 {
		return "", "", false
	}
	if parts[0] != "BenchmarkCorpus" {
		return "", "", false
	}
	return parts[1], parts[2], true
}

func groupByCategoryName(entries []entry) map[string]map[string]entry {
	out := map[string]map[string]entry{}
	for _, e := range entries {
		if _, ok := out[e.category]; !ok {
			out[e.category] = map[string]entry{}
		}
		out[e.category][e.name] = e
	}
	return out
}

func render(w *os.File, matrix map[string]map[string]entry, compileNS int64) {
	fmt.Fprintln(w, "# Benchmark — googlesqlite")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "Auto-generated by `bench/render_results.go`. Re-run `make bench`")
	fmt.Fprintln(w, "to refresh.")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "Per-query latency, throughput and allocations for the")
	fmt.Fprintln(w, "googlesqlite driver. Lower is better. To detect a regression,")
	fmt.Fprintln(w, "compare a fresh run against a saved baseline of this file.")
	fmt.Fprintln(w)

	if compileNS >= 0 {
		fmt.Fprintln(w, "## Runtime initialisation")
		fmt.Fprintln(w)
		fmt.Fprintf(w, "One-time wazero AOT compilation of the embedded GoogleSQL\n")
		fmt.Fprintf(w, "wasm (cold cache): **%s ns** (%.2f s).\n",
			commaSeparate(strconv.FormatInt(compileNS, 10)),
			float64(compileNS)/1e9)
		fmt.Fprintln(w)
		fmt.Fprintln(w, "This cost is paid once per process. The runtime is warmed")
		fmt.Fprintln(w, "before the corpus runs, so it is NOT included in the")
		fmt.Fprintln(w, "per-query numbers below.")
		fmt.Fprintln(w)
	}

	categories := keys(matrix)
	sort.Strings(categories)
	// Surface window first because it is the heaviest workload.
	preferred := []string{"window", "aggregate", "scalar", "mixed", "tpch_like"}
	categories = orderBy(categories, preferred)

	for _, cat := range categories {
		fmt.Fprintf(w, "## %s\n\n", strings.Title(cat)) //nolint:staticcheck // titlecase ASCII is fine here
		fmt.Fprintln(w, "| Query | ns/op | B/op | allocs/op |")
		fmt.Fprintln(w, "|-------|-------|------|-----------|")
		names := keys(matrix[cat])
		sort.Strings(names)
		for _, name := range names {
			e := matrix[cat][name]
			fmt.Fprintf(w, "| %s | %s | %s | %s |\n",
				name,
				formatBigFloat(e.nsPerOp),
				formatBigInt(e.bytesOp),
				formatBigInt(e.allocsOp),
			)
		}
		fmt.Fprintln(w)
	}
}

// formatBigFloat writes an ns/op value with thousand separators.
func formatBigFloat(v float64) string {
	if v < 1000 {
		return fmt.Sprintf("%.0f", v)
	}
	s := fmt.Sprintf("%.0f", v)
	return commaSeparate(s)
}

func formatBigInt(v int64) string {
	return commaSeparate(strconv.FormatInt(v, 10))
}

func commaSeparate(s string) string {
	neg := strings.HasPrefix(s, "-")
	if neg {
		s = s[1:]
	}
	var b strings.Builder
	for i, r := range s {
		if i > 0 && (len(s)-i)%3 == 0 {
			b.WriteByte(',')
		}
		b.WriteRune(r)
	}
	if neg {
		return "-" + b.String()
	}
	return b.String()
}

func keys[V any](m map[string]V) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	return out
}

// orderBy returns the elements of all in the order they appear in
// preferred (matched first), then any remaining elements in their
// original order.
func orderBy(all, preferred []string) []string {
	out := []string{}
	seen := map[string]bool{}
	for _, p := range preferred {
		for _, a := range all {
			if a == p {
				out = append(out, a)
				seen[a] = true
			}
		}
	}
	for _, a := range all {
		if !seen[a] {
			out = append(out, a)
		}
	}
	return out
}

func must(err error) {
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}
